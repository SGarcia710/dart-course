// Final y Const
main(List<String> args) {
  // Final: Únicamente será alojada en memoria al momento de usarla. Es decir, al momento en el que realmente el código haga uso de ella.
  final String nombre = "Sebastián";
  print(nombre);

  // Const: Es declara y alojada en memoria al momento de ponerla en nuestro código.
  const edad = 23;
  print(edad);
}
